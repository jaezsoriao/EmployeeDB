using System;

namespace EmployeeNamespace
{
    public struct Employee
    {
        private int id;
        private string firstName, lastName, position;

        public int ID
        {
            get { return id; }
            set { id = value; }
        }
        public string FirstName
        {
            get { return firstName; }
            set { firstName = value; }
        }
        public string LastName
        {
            get { return lastName; }
            set { lastName = value; }
        }
        public string Position
        {
            get { return position; }
            set { position = value; }
        }
    }

}
